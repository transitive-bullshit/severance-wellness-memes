async function main() {
  const wellnessSessions = await prisma.wellnessSession.findMany({
    include: {
      //   wellnessFacts: true,
      //   pinnedWellnessFact: true,
      twitterUser: {
        select: { user: true, status: true }
      }
    }
  })

  const tuToC = Object.fromEntries(
    tu.map((t) => [t.twitterUsername, t.category])
  )

  const results = (
    await pMap(
      wellnessSessions,
      async (wellnessSession) => {
        const { twitterUsername, twitterUser, userFullName } = wellnessSession

        const user = twitterUser?.user!
        if (!user) return

        const userFullNameParts = userFullName
          ?.split(' ')
          .map((s: string) => s.trim())
          .filter(Boolean)
        const userFirstName =
          userFullName &&
          (userFullNameParts!.length === 2 || userFullNameParts!.length === 1)
            ? userFullNameParts![0]
            : undefined

        const name = userFirstName ? userFirstName + ',' : 'ðŸ‘‹'
        const url = `${config.prodUrl}/x/${twitterUsername}`
        const category = tuToC[twitterUsername]
        let text: string

        if (category === 'g') {
          text = `Hey ${name} I created some fun Severance memes based on your twitter profile + tweets.\n\nYou can check them out here ${url}\n\nLemme know what you think :)`
        } else if (category === 'f') {
          text = `Hey ${name} I created some fun AI-generated Severance memes based on your twitter profile + tweets. ${url}\n\nThis project is open source and purely for fun, so no worries if Severance isn't your thing, but if you vibe w/ the memes, lemme know your favorite ones by responding to or retweeting this launch thread ðŸ™ ${config.twitterLaunchThreadUrl}`
        } else if (category === 's') {
          text = `Hey ${name} I noticed you're into Severance memes, so I created some wellness memes based on your twitter profile + tweets. ${url}\n\nIf you vibe w/ the memes, lemme know your favorite ones by responding to or retweeting this launch thread: ${config.twitterLaunchThreadUrl}\n\nThanks && Praise Kier ðŸ™`
        } else {
          return
        }

        return {
          twitterUsername,
          category,
          text
        }
      },
      {
        concurrency: 8
      }
    )
  )
    .filter(Boolean)
    .sort((a, b) => a.category.localeCompare(b.category))

  // console.log(JSON.stringify(results, null, 2))
  console.log(
    results
      .map((r) => `${r.twitterUsername}\t${r.category}\t${r.text}`)
      .join('\n')
  )
}

try {
  restoreCursor()
  await main()
  gracefulExit(0)
} catch (err) {
  console.error('error', err)
  gracefulExit(1)
}
